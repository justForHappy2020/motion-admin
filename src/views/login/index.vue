<script>
  // import {
  //   validUsername
  // } from '@/utils/validate'

  import{
    getCode
  }from '@/api/user'
import { getToken } from '@/utils/auth'

  export default {
    name: 'Login',
    data() {
      const validatePhone = (rule, value, callback) => {
        if (value.length != 11) {
          callback(new Error('请输入正确的手机号'))
          //是否可以发送验证码
          this.disabled = true;
        } else {
          callback()
          this.disabled = false;
        }
      }
      const validateCode = (rule, value, callback) => {
        if (!value) {
          callback(new Error('请输入正确的验证码'))
        } else {
          callback()
        }
      }
      return {
        //  usedata:{
        // userld:0,
        // phoneNumber:'',
        // nickName:'',
        // headPortrait:'',
        // type:0,
        // password:'',
        // token:getToken(),
        // gender :0,
        // intro:'',
        // birthday:'',
        // createTime:'',
        // },
        code:0,
        loginForm: {
          phoneNumber: '',
          code: ''
        },
        loginRules: {
          phoneNumber: [{
            required: true,
            trigger: 'change',
            validator: validatePhone
          }],
          code: [{
            required: true,
            trigger: 'change',
            validator: validateCode
          }]
        },
        loading: false,
        // passwordType: 'password',
        redirect: undefined,
        btnTitle: "获取验证码",
        disabled: '' //是否可点击
      }
    },
    watch: {
      $route: {
        handler: function(route) {
          this.redirect = route.query && route.query.redirect
        },
        immediate: true
      }
    },
    //   created() {
    //   this.judglogin()
    // },
    methods: {
      // showPwd() {
      //   if (this.passwordType === 'password') {
      //     this.passwordType = ''
      //   } else {
      //     this.passwordType = 'password'
      //   }
      //   this.$nextTick(() => {
      //     this.$refs.password.focus()
      //   })
      // },
      handleLogin() {
        this.$refs.loginForm.validate(valid => {
          if (valid) {
            this.loading = true
            this.$store.dispatch('user/login', this.loginForm).then(() => {
              console.log('success login')
              this.$router.push({
                path: this.redirect || '/'
              })
              this.loading = false
            }).catch(() => {
              this.loading = false
            })
          } else {
            console.log('error submit!!')
            return false
          }
        })
      },
      //验证token
      // judglogin(){
      //  
      //     this.usedata=response.data})
        
      // },
      //请求发送验证码
      getCode() { 
        // login(getToken()).then(response=>{
        //   console.log(response.data)});
          this.codeWaitTime();
          getCode(this.loginForm).then(response => {
            this.loginForm.code = response.data;
             console.log(response.data);
            console.log(this.loginForm.code)
          })
      },
      //验证码倒计时
      codeWaitTime() {
        let time = 60;
        let timer = setInterval(() => {
          if (time == 0) {
            clearInterval(timer);
            this.disabled = false;
            this.btnTitle = "获取验证码";
          } else {
            this.btnTitle = time + '秒后重试';
            this.disabled = true;
            time--
          }
        }, 1000)
      }
    }
  }
</script>

